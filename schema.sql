drop table if exists band_name_genre_counts;
drop table if exists yay_nays;
drop table if exists band_names;
drop table if exists genres;

create table band_names
(
    id   integer primary key autoincrement,
    name text not null unique
);

create table genres
(
    id   integer primary key autoincrement,
    name text not null unique
);

create table band_name_genre_counts
(
    band_name_id integer not null,
    genre_id     integer not null,
    total        integer default 0,

    primary key (band_name_id, genre_id),

    foreign key (band_name_id) references band_names (id) on delete cascade,
    foreign key (genre_id) references genres (id) on delete cascade,

    unique (band_name_id, genre_id)
);

create table yay_nays
(
    band_name_id integer primary key,
    yay_count integer default 0,
    nay_count integer default 0,

    foreign key (band_name_id) references band_names (id) on delete cascade
);

